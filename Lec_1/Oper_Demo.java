package Lec_1;

public class Oper_Demo {

	public static void main(String[] args) {
				int a = 89;
				int b = 50;

				// +
				int c = a + b;
				System.out.println(c);

				// -
				c = a - b;
				System.out.println(c);
				
				// *
				c=a*b;
				System.out.println(c);
				
				// /
				c=a/b;
				System.out.println(c);
	}

}
